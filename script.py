        self.delay = delay
        self.photo = None
        self.audio = None
        if photo is not None:
            self.photo = './static/photos/' + photo + '.jpg'
        if audio is not None:
            self.audio = './static/audio/' + audio + '.mp3'
        self.preview = preview


